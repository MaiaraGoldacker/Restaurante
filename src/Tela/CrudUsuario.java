    private boolean alteraRegistro() {
        return edId.getText().equalsIgnoreCase("");
    }
    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        try {
            if (alteraRegistro()) {
                JOptionPane.showMessageDialog(null, "Selecione um registro para alterar");
            } else {

                String retorno = RegrasGerenciamento.getInstance().alterarUsuario(Integer.parseInt(edId.getText()), edUsuario.getText(), ednomeUser.getText(), edSenha.getText(), edSenhaConfirmar.getText(), cbPermissao.getSelectedIndex());

                if (retorno.equalsIgnoreCase("")) {
                    carregaTabela();
                    JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
                    limpa();
                } else {
                    JOptionPane.showMessageDialog(null, retorno);
                }
            }
        } catch (InvalidKeySpecException ex) {
            Logger.getLogger(CrudUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CrudUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(CrudUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchProviderException ex) {
            Logger.getLogger(CrudUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (alteraRegistro()) {
            JOptionPane.showMessageDialog(null, "Selecione um registro para excluir");
        } else {
            RegrasGerenciamento.getInstance().excluirUsuario(Integer.parseInt(edId.getText()));
            JOptionPane.showMessageDialog(null, "Excluído com sucesso!");
            carregaTabela();
            limpa();
        }
    }//GEN-LAST:event_btExcluirActionPerformed

