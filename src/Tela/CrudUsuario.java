    private void carregaPrimeiroLogin() {
        try {
            Usuario usuario = UsuarioDAO.getInstance().findByUser("admin");
            edId.setText(String.valueOf(usuario.getId()));
            edUsuario.setText(usuario.getDsusuario());
            ednomeUser.setText(usuario.getNmusuario());
            cbPermissao.setSelectedIndex(usuario.getIetipopermissao());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar o primeiro Login. Contate um programador! " + e);
        }
    }

    private boolean verificaPrimeiroLogin() {
        boolean alterou = true;
        try {
            Usuario usuario = UsuarioDAO.getInstance().findByUser("admin");
            if (usuario.getId() != null) {

                String securePassword = Passwords.getInstance().getSecurePassword("admin", usuario.getSalt());
                if (usuario.getDssenha().equalsIgnoreCase(securePassword)) {
                    alterou = false;
                    JOptionPane.showMessageDialog(null, "Senha administrativa precisa ser trocada no primeiro login!");
                }
            }
            return alterou;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao verificar primeiro login, verifique! " + e);
            return false;
        }
    }

