    public void carregaTabela() {
        List<Adicional> lista = new ArrayList<Adicional>();
        listaAdicionaisNaoInseridos.clear();
        boolean adiciona = true;
        try {
            lista = AdicionaisDAO.getInstance().findAll();

            if (listaAdicionaisInseridos.size() != 0) {
                for (Adicional listaTodosAdicionais : lista) {

                    for (Adicional listaCarregar : listaAdicionaisInseridos) {
                        if (listaCarregar.getId() == listaTodosAdicionais.getId()) {
                            adiciona = false;
                        }
                    }
                    if (adiciona) {
                        listaAdicionaisNaoInseridos.add(listaTodosAdicionais);
                    }
                    adiciona = true;
                }
            } else {
                listaAdicionaisNaoInseridos = lista;
            }

            AdicionalJtable tm = new AdicionalJtable(listaAdicionaisNaoInseridos);
            tbAdicionais.setModel(tm);
        } catch (SQLException ex) {
            Logger.getLogger(CrudProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void carregarTabelaAdicionaisInseridos() {
        try {
            listaAdicionaisInseridos.clear();
            List<AdicionalPedido> adicionaisPed = AdicionalPedidoDAO.getInstance().findAdicionaisDeProdutos(this.idPedido, this.idProduto);

            if (adicionaisPed.size() != 0) {
                for (AdicionalPedido adicionaisEncontrados : adicionaisPed) {
                    listaAdicionaisInseridos.add(AdicionaisDAO.getInstance().getById(adicionaisEncontrados.getAdicionalId()));
                }
            } else {
                listaAdicionaisInseridos.clear();
            }
            AdicionalJtable tm = new AdicionalJtable(listaAdicionaisInseridos);
            tbAdicionaisSelecionados.setModel(tm);
        } catch (SQLException ex) {
            Logger.getLogger(CrudProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

