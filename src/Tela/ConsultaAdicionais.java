    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        try {
            List<Adicional> com = new ArrayList<Adicional>();

            int[] linhas = tbAdicionais.getSelectedRows();
            for (int i = 0; i < linhas.length; i++) {

                Adicional adicional = new Adicional();
                if (listaAdicionaisInseridos.size() != 0) {
                    AdicionalJtable tm;
                    tm = new AdicionalJtable(listaAdicionaisNaoInseridos);
                    adicional = tm.get(linhas[i]);
                } else {
                    AdicionalJtable tm;
                    tm = new AdicionalJtable(null);
                    adicional = tm.get(linhas[i]);
                }
 
                RegrasGerenciamento.getInstance().salvarAdicionalPedido(adicional.getId(), this.idPedido, this.idProduto);             
                carregarTabelaAdicionaisInseridos();
                carregaTabela();
            }

        } catch (SQLException ex) {
            Logger.getLogger(ConsultaAdicionais.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        try {
            int[] linhas = tbAdicionaisSelecionados.getSelectedRows();
            for (int i = 0; i < linhas.length; i++) {

                Adicional adicional = new Adicional();
                if (listaAdicionaisInseridos.size() != 0) {
                    AdicionalJtable tm;
                    tm = new AdicionalJtable(listaAdicionaisInseridos);
                    adicional = tm.get(linhas[i]);
                } else {
                    AdicionalJtable tm;
                    tm = new AdicionalJtable(null);
                    adicional = tm.get(linhas[i]);
                }

                List<AdicionalPedido> adicionaisPed = AdicionalPedidoDAO.getInstance().findAdicionalPedidos(adicional.getId(), this.idProduto, this.idPedido);

                for (AdicionalPedido adicionaisPedido : adicionaisPed) {
                    AdicionalPedidoDAO.getInstance().remove(adicionaisPedido);
                }

                carregarTabelaAdicionaisInseridos();
                carregaTabela();

            }

        } catch (SQLException ex) {
            Logger.getLogger(ConsultaAdicionais.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btRemoverActionPerformed

