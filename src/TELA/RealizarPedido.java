    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        try {
            if (!edComanda.getText().equalsIgnoreCase("")) {
                PedidoJtable tm = new PedidoJtable(null, Integer.parseInt(edComanda.getText()));

                int[] linhas = tbPedidos.getSelectedRows();
                for (int i = 0; i < linhas.length; i++) {
                    Pedido obj = tm.get(linhas[i]);
                    if (obj.getStpedido() == 2 && comandaExterna == 0) {
                        JOptionPane.showMessageDialog(null, "Este pedido já foi atendido e não pode ser removido. Caso necessário altere no Pagamento da Comanda!");
                    } else {
                        PedidoDAO.getInstance().removeById(obj.getId());
                        carregarTabelaPedido();
                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma comanda para alteração!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(RealizarPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

