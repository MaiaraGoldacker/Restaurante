    public void carregarTabelaPedido() {
        List<Pedido> lista;
        try {
            lista = null;

            if (edComanda.getText().equalsIgnoreCase("")) {
                PedidoJtable tm = new PedidoJtable(null, 0);

                tbPedidos.setModel(tm);
            } else if (!"".equalsIgnoreCase(edComanda.getText())) {
                lista = PedidoDAO.getInstance().findAllAtendimento(3, Integer.parseInt(edComanda.getText()));
                PedidoJtable tm = new PedidoJtable(lista, 0);
                tbPedidos.setModel(tm);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CrudProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void carregaTabela(List<Produto> produtos) {
        try {
            ProdutoJtable tm = new ProdutoJtable(produtos);
            tbProdutos.setModel(tm);
            carregarTabelaPedido();
        } catch (SQLException ex) {
            Logger.getLogger(CrudProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

