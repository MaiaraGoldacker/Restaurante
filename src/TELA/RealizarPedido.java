    private void cbClassificacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbClassificacaoActionPerformed
        try {
            if (cbClassificacao.getSelectedIndex() == 0) {
                carregaTabela(null);
            } else {
                List<Produto> prods = ProdutoDAO.getInstance().findAllbyClassif(cbClassificacao.getSelectedIndex());

                carregaTabela(prods);
                if (prods.size() == 0) {
                    edComanda.setText("");
                }
                carregarTabelaPedido();
            }
        } catch (SQLException ex) {
            Logger.getLogger(RealizarPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cbClassificacaoActionPerformed

