    private void limpa() {
        edId.setText("");
        edObservacao.setText("");
    }

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if ("".equalsIgnoreCase(RegrasGerenciamento.getInstance().salvarComanda(edObservacao.getText()))) {
            carregaTabela();
            JOptionPane.showMessageDialog(null, "Adicionado com sucesso!");
            limpa();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLimparActionPerformed
        limpa();
    }//GEN-LAST:event_btLimparActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed

        if ("".equalsIgnoreCase(RegrasGerenciamento.getInstance().alterarComanda(Integer.parseInt(edId.getText()), edObservacao.getText()))) {
            carregaTabela();
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
            limpa();
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        RegrasGerenciamento.getInstance().excluirComanda(Integer.parseInt(edId.getText()));
        JOptionPane.showMessageDialog(null, "Excluído com sucesso!");
        carregaTabela();
        limpa();
    }//GEN-LAST:event_btExcluirActionPerformed

