    private void tbComandasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbComandasMouseClicked
        try {
            int linha = tbComandas.getSelectedRow();
            ComandaJtable tm = new ComandaJtable();
            Comanda com = tm.get(linha);
            edId.setText(String.valueOf(com.getId()));
            edId.setText(Integer.toString(com.getId()));
            edObservacao.setText(com.getDsobservacao());

        } catch (SQLException ex) {
            Logger.getLogger(CrudProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_tbComandasMouseClicked

    private void btImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btImprimirActionPerformed
        IPedidosReport i = new IPedidosReport();
        try {
            i.gerarRelatorioPadrao("C:\\Atividade\\Pedidos\\src\\IREPORTXML\\REL05RelatorioComandas.jasper", "Relatório de Comandas");
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaLucro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btImprimirActionPerformed

